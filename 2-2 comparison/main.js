var sajibAge = 23; 
var shawnAge = 22; 

// == Operator
console.log(sajibAge == shawnAge); 

// === Operator
console.log(sajibAge === shawnAge); 

// != operator 
console.log (sajibAge != 100);

// !== Operator 
console.log(sajibAge !== shawnAge); 

// <	less than
console.log(sajibAge < shawnAge); 

// >	greater than
console.log (shawnAge > sajibAge); 

// <= greater than or equal to 
console.log(sajibAge <= shawnAge); 

// =< less than or equal to 
console.log (shawnAge >= sajibAge); 